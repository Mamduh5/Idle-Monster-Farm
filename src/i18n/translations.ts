const TABLES: Record<LanguageCode, TranslationTable> = {
  en: EN,
  th: TH,
};

export function getDefaultLanguage(): LanguageCode {
  try {
    return navigator.language.toLowerCase().startsWith('th') ? 'th' : 'en';
  } catch {
    return 'en';
  }
}

export function isLanguageCode(value: unknown): value is LanguageCode {
  return value === 'en' || value === 'th';
}

export function getText(language: LanguageCode, key: string, params: TranslationParams = {}): string {
  const template = TABLES[language][key] ?? EN[key] ?? key;

  return template.replace(/\{(\w+)\}/g, (_, paramKey: string) => {
    const value = params[paramKey];

    return value === undefined ? `{${paramKey}}` : String(value);
  });
}
